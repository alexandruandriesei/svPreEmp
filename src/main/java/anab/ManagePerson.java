package anab;

public class ManagePerson {

    private String firstName;
    private String lastName;
    private int age;
    private char gender;
    private char email_address;

    public ManagePerson(String firstName, String lastName, int age, char gender, char email_address) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.age = age;
        this.gender = gender;
        this.email_address = email_address;
    }
}
